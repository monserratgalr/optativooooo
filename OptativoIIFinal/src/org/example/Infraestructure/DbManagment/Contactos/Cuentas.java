public class Cuentas {
    
    private int id_cuenta;
    private int id_cliente;
    private String num_cuenta;
    private String fecha_alta;
    private String tipo_cuenta;
    private String estado;
    private String saldo;
    private String num_contrato;
    private String costo_mantenimiento;
    private String prom_acreditacion;
    private String moneda;

    public int getId_cuenta() {
        return id_cuenta;
    }
    public void setId_cuenta(int id_cuenta) {
        this.id_cuenta = id_cuenta;
    }
    public int getId_cliente() {
        return id_cliente;
    }
    public void setId_cliente(int id_cliente) {
        this.id_cliente = id_cliente;
    }
    public String getNum_cuenta() {
        return num_cuenta;
    }
    public void setNum_cuenta(String num_cuenta) {
        this.num_cuenta = num_cuenta;
    }
    public String getFecha_alta() {
        return fecha_alta;
    }
    public void setFecha_alta(String fecha_alta) {
        this.fecha_alta = fecha_alta;
    }
    public String getTipo_cuenta() {
        return tipo_cuenta;
    }
    public void setTipo_cuenta(String tipo_cuenta) {
        this.tipo_cuenta = tipo_cuenta;
    }
    public String getEstado() {
        return estado;
    }
    public void setEstado(String estado) {
        this.estado = estado;
    }
    public String getSaldo() {
        return saldo;
    }
    public void setSaldo(String saldo) {
        this.saldo = saldo;
    }
    public String getNum_contrato() {
        return num_contrato;
    }
    public void setNum_contrato(String num_contrato) {
        this.num_contrato = num_contrato;
    }
    public String getCosto_mantenimiento() {
        return costo_mantenimiento;
    }
    public void setCosto_mantenimiento(String costo_mantenimiento) {
        this.costo_mantenimiento = costo_mantenimiento;
    }
    public String getProm_acreditacion() {
        return prom_acreditacion;
    }
    public void setProm_acreditacion(String prom_acreditacion) {
        this.prom_acreditacion = prom_acreditacion;
    }
    public String getMoneda() {
        return moneda;
    }
    public void setMoneda(String moneda) {
        this.moneda = moneda;
    }    
}
    
    

